#include <ctype.h>
#include "libft.h"
#include <stdio.h>

/* Esta funciÃ³n devuelve un 1 si es un caracter
 * alfabetico, y un 0 si no lo es*/

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}
